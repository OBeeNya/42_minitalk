#include "minitalk.h"

void	reverse_tab(int *b)
{
	int	i;
	int	temp;

	i = 0;
	while (i < 4)
	{
		temp = b[i];
		b[i] = b[7 - i];
		b[7 - i] = temp;
		i++;
	}
}
